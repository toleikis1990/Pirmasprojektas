package ciklai;

public class WhileLoopDemo {

    public static void main(String[] args) {

        int a = 0;

        // While ciklo veikimas:
        // Kodo blokas vykdomas tol kol sąlyga (šiuo atveju: a < 5) gražina true reikšmę
        while (a < 5) {
            System.out.println("Fraze");
            a++;
        }

    }
}
