package strings;

public class StringConcatenationDemo {

    public static void main(String[] args) {
        int rezultatas = 10;
        // Sujungimas (Concatenation)
        System.out.println("Atsakymas: " + rezultatas);
        System.out.println("Atsakymas: " + rezultatas + ". Valio" + ". Ura");

        // Atkreipkite dėmesį į tokį atvejį (jeigu spausdinant norite atlikti operaciją -
        // apskliauskite ją):
        System.out.println("Atsakymas: " + (2 + 3));
    }
}
